function languageMove(lan) {
	const html = document.querySelector('html');
	let l = MESS_ENG;
	if (lan === 'rus') { 
		l = MESS_RUS; 
		html.attributes.lang.textContent = 'ru';
	}
	else if (lan === 'ukr') { 
		l = MESS_UKR; 
		html.attributes.lang.textContent = 'ua';
	}
	else { 
		l = MESS_ENG;
		html.attributes.lang.textContent = 'en';
	}

	const sidebar = document.querySelector('.sidebar');
	const menuCenter = sidebar.querySelector('.menu-center');
	const menuCenterTextList = sidebar.getElementsByClassName('menu-center-text');

	if (menuCenterTextList) {
		(menuCenterTextList[0].querySelector('span')).innerText = l.sidebar.home;
		(menuCenterTextList[1].querySelector('span')).innerText = l.sidebar.about;
		(menuCenterTextList[2].querySelector('span')).innerText = l.sidebar.events;
		(menuCenterTextList[3].querySelector('span')).innerText = l.sidebar.news;
		(menuCenterTextList[4].querySelector('span')).innerText = l.sidebar.partners;
		(menuCenterTextList[5].querySelector('span')).innerText = l.sidebar.contscts;
	}

	const sectionOne = document.querySelector('.section_one');
	const contentTextSectionOne = sectionOne.querySelector('.content-text');
	
	const htmlTextSectionOne = `<p>${l.section_one.p_1}</p><p>${l.section_one.p_2}</p><p>${l.section_one.p_3}</p>`;
	contentTextSectionOne.innerHTML = htmlTextSectionOne;

	const sectionTwo = document.querySelector('.section_two');
	const contentTextSectionTwo = sectionTwo.querySelector('.content-text');

	const htmlTextSectionTwo = `<p>${l.section_two.p_1}</p>`;
	contentTextSectionTwo.innerHTML = htmlTextSectionTwo;


	const headerButton = document.querySelector('.header__butoons');
	const buttons = headerButton.getElementsByClassName('button');

	(buttons[0].querySelector('.button__link')).innerText = l.buttons.login;
	(buttons[1].querySelector('.button__link')).innerText = l.buttons.signup;


	const search = document.querySelector('.search');
	const inputSearch = search.querySelector('input');
	inputSearch.placeholder = l.input;

}